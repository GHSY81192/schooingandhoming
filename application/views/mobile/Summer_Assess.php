<script>
    var login_regPhone = /1[3|4|5|7|8|][0-9]{9}/;
    var login_regCode = /[0-9]{4}/;
    var login_isSend = false;
    $('.grade').click(function(){
        $('.select1').html($(this).html());
    });
    $('.age').click(function(){
        $('.select2').html($(this).html());
    });


    //点击发送验证码
    $(".code_btn").click(function(){
        if(login_isSend){
            return false;
        }
        var userPhone = $("#phone").val();
        if (!login_regPhone.test(userPhone)) {
            layer.alert('手机号码错误！');
            return false;
        }else{
            $.post('/web/sendAuthCode',{'mobile':userPhone},function(data){
                if(data.status){
                    login_isSend = true;
                    login_time2();
                }else{
                    layer.alert(data.msg);
                }
            },'json')
        }

    });

    //倒计时
    var login_wait = 60;
    function login_time2() {
        if (login_wait == 0) {
            $(".code_btn").removeAttr("disabled").val('发送验证码');
            login_isSend = false;
            login_wait = 60;
        } else {
            html = "重新发送(" + login_wait + ")";
            $(".code_btn").attr("disabled","disabled").val(html);
            login_wait--;
            setTimeout(function() {
                    login_time2()
                },
                1000);
        }
    }


    $('.submit_btn').click(function(){
        var data = {};
        data.grade = $('select[name=grade]').val();
        data.age = $('select[name=age]').val();

        var hobby = '';
        $('input[name=hobby]:checked').each(function(){
            hobby += $(this).val() + ',';
        });
        data.hobby = hobby;
        data.english_score = $('input[name=english_score]').val();
        var english = '';
        $('input[name=english]:checked').each(function(){
            english += $(this).val() + ',';
        });
        data.english = english;
        data.name = $('input[name=name1]').val();
        data.phone = $('input[name=phone]').val();
        data.code = $('input[name=code]').val();

        if(!data.name){
            layer.alert('请填写联系人姓名');
            return false;
        }
        if(!data.phone){
            layer.alert('请填写联系人电话');
            return false;
        }

        if (!login_regPhone.test(data.phone)) {
            layer.alert('联系人电话格式错误！');
            return false;
        }
        if (!login_regCode.test(data.code)) {
            layer.alert('验证码错误！');
            return false;
        }

        $.post('/web/summer_assess_submit',data,function(data){
            if(data.status === 444){
                layer.alert('验证码错误！');
                return false;
            }
            if(data.status === 888){
                layer.msg('提交成功！',{time:1000},function(){
                    location.reload();
                })
            }
        },'json')




    })

</script>