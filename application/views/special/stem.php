        <h3>什么是STEM教育</h3>
        <hr>
        <div class="row mb50">
            <img src="/public/images/special/stem/stem-1.gif" class="col-md-6 md6-left">
            <div class="col-md-6 md6-right">
                <p style="margin-bottom: 20px">STEM是科学（Science），技术（Technology），工程（Engineering），数学（Mathematics）四门学科英文首字母的缩写。<br>
STEM高中是美国教育一大特色，被称为美国各学区“皇冠上的宝石”，是一类专为有天赋、有才能，对科学、技术、工程、数学感兴趣的学生提供精英教育的学校。
不仅如此，STEM教育法还试图彻底改变以老师为中心的典型性课堂模式，鼓励发展一种问题解决式、探索发现式学习的课程模式，这个模式要求学生积极参与，以寻求解问题的解决方法。</p>
