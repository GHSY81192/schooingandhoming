                <h3 style="text-align: left;">为何选择有STEM教育的高中?</h3>
                <hr>
                <img src="/public/images/special/stem/p3-l.gif" width="100%" style="margin-bottom: 20px;">
                <p style="margin-bottom: 20px;">在过去的十年中，STEM领域的工作比起非STEM岗位多了三倍。通过美国商务部，STEM领域在2008-2018年需求预期增加至17%，这与同期非STEM领域的就业高出9.8%。当然没有毕业生填补这些领域的空缺美国将没有足够的工人去做这些岗位。STEM学校可以帮助年轻人在这些领域获取技术并取得成功。在2012年的一份科学与技术主席委员会建议书报道：</p>
                <p style="margin-bottom: 20px;">在未来的二十年里，美国必须产生比目前多100多万的STEM学位毕业生来满足就业经济形势。意识到这个差距后，更多的教育者们已经开始注重那些在学校生涯就被数学与科学吸引的学生们，这就是为什么现在各种STEM项目在全国范围启动。</p>
                <p>你会发现主要还都是STEM高中，但现在也有些中学有偏重STEM的课程背景。有些这种学校对所有学生开放，意味着这个就没有测试要求;其他的学校会比较挑学生并从需要学术背景等方面的考量。</p>
            </div>
        </div>
