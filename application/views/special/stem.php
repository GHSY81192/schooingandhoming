            <div class="part1-box">
                <h3 style="text-align: left;">STEM高中的优势?</h3>
                <hr>
                <img src="/public/images/special/stem/p3-r.gif" width="100%" style="margin-bottom: 20px;">
                <p style="font-size: 16px;">STEM高中培养学生能力为主：</p>
                <ul class="stem-ul" style="padding-left: 0">
                    <li>9年级，学生关注多样性，了解自己，学习如何与和自己不同的人一起工作。</li>
                    <li>10年级，学生学习面对一个一无所知的难题时，团队伙伴们如何相互负责。</li>
                    <li>11年级，学生学习在个人层面和团队层面如何做好时间管理。学生需要把握好 项目各阶段的时间和空间，才能保证项目最终完成。</li>
                    <li>12年级，学生学习“超越校园的合作”，他们主动去寻找自己感兴趣的专家，并与他们建立联系。他们学习如何创业，并获得校外“老师”的建议。</li>
                </ul>
