        导语：夏校 Summer School 起源于美国，又被简称为暑校或者是暑期课程，是一种以学为主的暑期游学方式，这不同于传统的夏令营。在美国，每年暑假6月下旬至8月底，美国许多高校为国内外高中生和本科生开设专业课或语言课程，被Summer School录取的学生可直接进入国外名校进行短期学习，如哈佛、耶鲁、哥伦比亚、斯坦福等美国名校。无论是未来对留学有所规划，或者是仅仅对海外教育经历感兴趣的家庭都是性价比极高的一种选择。
