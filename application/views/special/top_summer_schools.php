            <p class="p16 cl-b">
                Summer School课程分为学分课程、非学分课程和语言课程
            </p>
            <p class="p14 cl-b fbt" >学分课程：</p>
            <p>为学生提供了一个在短期时间内修得美国大学学分的机会，学生可选修自己感兴趣的专业，获得官方学分，学分在各院校间互相承认，在正式留学后，可以实现学分转换，从而免修相关课程。
            </p>
            <p class="p14 cl-b fbt" >非学分课程：</p>
            <p>能让学生根据自己的兴趣爱好任选课程学习，感受原汁原味的美国大学课堂，课程结束时考试成绩合格，部分学校可颁发相应结业证书。<br>语言课程：全面提高学生的听说读写能力。</p>
            <p class="p14 cl-b fbt" >语言课程：</p>
            <p>全面提高学生的听说读写能力。参加语言课程学习，能够让国际生全方位强化听、说、读、写能力，有些学校甚至开设托福和“学术能力评估测试”(SAT)的相关辅导课程，为高中生申请大学做准备。</p>
        </div>

        <div class="col-md-6 md6-r">
            <h3>申请夏校需要什么条件？</h3>
            <hr>
            <img class="img-1" src="/public/images/special/top_summer_schools/夏校专题页_03_05.gif">
            <p class="p16 cl-b mb30">
                1.参加Summer School是否需要托福成绩？
            </p>
            <p>因课程设置不同，要求也不相同。学分课程需要提供托福成绩，而且每个学校的Summer School项目对托福成绩的要求都不相同。像Harvard、Yale、Cornell等这样的大学基本都要求TOEFL100分的。大部分非学分课程和语言课程则不需要托福成绩。
            </p>

            <p class="p16 cl-b mb30 mt30">
                2.申请Summer School需要准备哪些材料？
            </p>
            <p>
                材料通常为三类：一类是与现在国内就读学校相关的材料，即成绩单和在读证明；二类是与资金相关的材料，即资金证明和申请费；第三类则是与美国学校相关的材料了，包含文书（简历、推荐信、essays等）和语言成绩（如果有）。
            </p>

            <p class="p16 cl-b mb30 mt30">
                3.夏校申请时间？
            </p>
            <p>美国夏校从6月-8月，课程从两周到八周不等，有些项目是和国内高中时间冲突的，提交材料基本在前一年的12月份。
            </p>
        </div>
    </div>


