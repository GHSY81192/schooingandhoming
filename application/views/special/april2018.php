            </div>
            <div class="col-md-6">
                <p class="cl333 hl">Carson原就读于大连某公立小学，是个体制内的牛娃，成绩在年级里名列前茅。他热爱数学，多次参加数学竞赛，同时对机械、编程、天文、物理等科目也怀有浓厚的兴趣。但就英文能力而言，Carson最初的英文水平相比于一线城市国际或双语学校背景的同学存在不小的差距。基于此，我们对Carson最初的规划建议是可以先去夏令营看看，体验一下美式的学习生活环境。2015-2017年，Carson连续三年参加了UP教育在美国缅因州为期4周的Hidden Valley Camp暑期项目。夏令营生活对他的英文提升帮助很大，从第一年的几乎听不懂到第三年在营地交到很多美国朋友，他觉得非常开心。<br><br></p>
                <p class="cl333 hl">经过三年的相处，Carson一家跟我们建立起了相当的信任，他的美初申请也就顺理成章地委托给了我们。针对他英文阅读薄弱且词汇欠缺的问题，我们为他设计了详细的阶段性提升目标。当然，他个人也非常努力，前后参加了几次TOEFL Junior考试，考试成绩一路飙升，从最初的700多一直到最后拿下850的高分。而针对美初的入学面试，我们特意为他请到了一位美高招生官进行系统性的在线培训和模拟面试， 他跟招生官的沟通也取得了突飞猛进的进步。应该说，Carson最终能够拿下Rectory和Indian Mountain，得益于孩子本人和我们的协同努力</p>
