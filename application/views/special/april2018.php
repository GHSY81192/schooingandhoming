            <div class="col-md-6 md6r">
                <p class="cl333 hl">第一次见到Oscar是他去年报名参加Fessenden Summer School的时候，一个外向、开朗、活泼的阳光男孩。Oscar就读于上海星河湾双语学校，一所令人非常向往的高品质民办双语学校。良好的培养土壤，成就了Oscar优秀的学术成绩，Oscar的兴趣爱好也随之见涨。酷爱运动、绘画与音乐，爱交朋友，喜欢动物，热爱生活。夏令营期间，Oscar的学术能力、综合实力以及性格等得到了老师们的高度认可。当Fessenden招生处对参加夏令营的国际学生抛出橄榄枝时，我们在评估后将Oscar推荐给了招生官。虽然这次面试并不能决定Oscar申请Fessenden成功与否，但面试的机会和Oscar的表现为后面的申请留下了伏笔。<br><br></p>
                <p class="cl333 hl">孩子的综合能力、家庭状况，优秀的顾问团队都具备了，Fessenden School的录取通知书似乎是如期而至。5周夏校的相处和了解，是发掘从中国来的优秀学生的最好的方式。Oscar 被录取，可以说是得益于前期家庭的培养、我们长期与学校合作的招生策略、申请阶段恰到好处的安排，一切似乎都是顺势而为、水到渠成。没有任何高压和考培、超出能力的背景拔分之类的申请难度。可以说，只要创造机会让学校了解到学生的能力，中国孩子的名校录取之路并不如想像中的艰辛。</p>
