                <p class="cl333 hl">但是来自中国的亚裔男生，是藤校录取的最弱势群体，Eric在体能上的弱小，无法与白人学生抗衡体育，变得样样好，样样不精。在11年级暑期，我们建议他参加了宾大环境建筑专业的夏校，利用他绘画的特长，在夏校完成了作品集，以此作为申请环境建筑方向的附加材料，终于获得了宾大的青睐，被提前录取。Eric 同学的案例，见证了一个中国公立学校男孩子，完全融入美国白人精英学校的过程。在他高中毕业时，阳光的个性，得体的谈吐，充满活力的外型，流利标准的中英文发音，无不彰显未来世界精英的形象。</p>
