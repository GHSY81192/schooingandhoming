        <h1>美高竞争加剧，美初受热捧</h1>
        <div class="img1">
            <img src="/public/images/special/april2018/april2018_activity-1.jpg">
        </div>
        <p class="p1">在这个时代，父母送孩子低龄留学最基本的目标是希望孩子能进入世界一流大学，未来能拥有更好的前程，实现社会阶层的跃升。要达成这个目标，不论是进入一流大学必须经历的申请与面试，还是成为未来的社会精英，前提都是必须要具备强大的语言掌控能力！这种能力的培养需要长时间的文化浸润，而这恰恰是美国初中的培养重点！<br>
初中的孩子，正处于青春期“三观”形成的阶段，在这一时期，周边环境和伙伴对他的影响甚至大于父母！美国初中，特别是传承百年的精英初中，教给孩子正确的“三观”、理性的做事逻辑、高效的时间管理，特别是长期来自西方经典文学艺术作品的熏陶，都会对孩子思维模式和趣味取向的形成起到极为积极且正面的影响！</p>
